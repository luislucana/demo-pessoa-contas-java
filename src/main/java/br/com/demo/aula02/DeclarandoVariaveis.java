package br.com.demo.aula02;

public class DeclarandoVariaveis {

    public static void main(String[] args) {
        String palavra = "computador";
        int numeroInteiro = 5; // numero inteiro
        double numeroDecimal = 3.4; // numero com casa decimal

        int outroNumeroInteiro = 8;
        int soma = numeroInteiro + outroNumeroInteiro;

        System.out.println(palavra);
        System.out.println(numeroInteiro);
        System.out.println(numeroDecimal);
        System.out.println(soma);

    }
}
